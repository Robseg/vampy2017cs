import turtle as t



def square(size):
	
	t.forward(size)
	t.left(90)
	t.forward(size)
	t.left(90)
	t.forward(size)
	t.left(90)
	t.forward(size)
	t.left(90)


def triangle(size):

	t.forward(size)
	t.left(120)
	t.forward(size)
	t.left(120)
	t.forward(size)
	t.left(120)
